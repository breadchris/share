package user

import (
	"context"
	"encoding/json"
	"net/http"

	"github.com/breadchris/share/pkg/httpcontext"
)

// RegisterHandlers registers all HTTP API endpoints with the provided ServeMux
func RegisterHandlers(mux *http.ServeMux) {
	mux.HandleFunc("/user/createtodo", handleCreateTodo)
	mux.HandleFunc("/user/listtodos", handleListTodos)
	mux.HandleFunc("/user/gettodo", handleGetTodo)
	mux.HandleFunc("/user/updatetodo", handleUpdateTodo)
	mux.HandleFunc("/user/deletetodo", handleDeleteTodo)
}

// RegisterWebSocketHandler registers the WebSocket endpoint
func RegisterWebSocketHandler(mux *http.ServeMux) {
	mux.HandleFunc("/user/ws", WebSocketHandler)
}

// HTTP handler functions
func handleCreateTodo(w http.ResponseWriter, r *http.Request) {
	if r.Method != http.MethodPost {
		http.Error(w, "Method not allowed", http.StatusMethodNotAllowed)
		return
	}

	var req CreateTodoRequest
	if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
		http.Error(w, "Invalid JSON", http.StatusBadRequest)
		return
	}

	// Create context with HTTP request and response writer
	ctx := httpcontext.WithHTTP(context.Background(), w, r)

	// Call the API function
	resp, err := CreateTodo(ctx, req)
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}

	w.Header().Set("Content-Type", "application/json")
	if err := json.NewEncoder(w).Encode(resp); err != nil {
		http.Error(w, "Failed to encode response", http.StatusInternalServerError)
	}
}

